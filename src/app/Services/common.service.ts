  public getAirLevelHighest() {
    let temp = 0;
    for (let air of this.airQualitys) {
      if (air.level > temp) {
        temp = air.level;
      }
    }
    return temp;
  }

  public getAirHighest() {
    let temp;
    for (let air of this.airQualitys) {
      if (air.level === this.getAirLevelHighest()) {
        temp = air;
      }
    }
    return temp;
  }

  /* Lấy bao nhiêu ngày */
  public getInfoDays(obj: any, numberDay: number) {
    let i = 0;
    for(let day of obj) {
      if((day.dayOfMonth == this.today.getDate()) && (day.month == this.today.getMonth() + 1) && (day.year == this.today.getFullYear())) {
        return obj.slice(i, i + numberDay);
      }
      i++;
    }
  }

  public getInfoDay(obj: any, date: Date) {
    for (let day of obj) {
      if((day.dayOfMonth == date.getDate()) && (day.month == date.getMonth() + 1) && (day.year == date.getFullYear())) {
        return day;
      }
    }
  }
